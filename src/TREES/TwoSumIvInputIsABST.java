package TREES;

//leetcode 653
// https://leetcode.com/problems/two-sum-iv-input-is-a-bst/description/

public class TwoSumIvInputIsABST {

    class TreeNode{
       TreeNode left;
       TreeNode right;
       int val;

        public TreeNode() {
        }

        public TreeNode(TreeNode left, TreeNode right, int val) {
            this.left = left;
            this.right = right;
            this.val = val;
        }

        public TreeNode(int val) {
            this.val = val;
        }
    }

    

}
