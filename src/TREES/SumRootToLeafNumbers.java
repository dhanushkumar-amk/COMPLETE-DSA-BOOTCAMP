package TREES;

// https://leetcode.com/problems/sum-root-to-leaf-numbers/
// leetcode 129

public class SumRootToLeafNumbers {
    public class TreeNode {
      int val;
      TreeNode left;
      TreeNode right;
      TreeNode() {}
      TreeNode(int val) { this.val = val; }
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
  }

  
}
