package TREES;


// https://leetcode.com/problems/symmetric-tree/description/

// leetcode 101




public class SymmetricTree {


     public class TreeNode {
      int val;
      TreeNode left;
      TreeNode right;
      TreeNode() {}
      TreeNode(int val) { this.val = val; }
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
  }

  private TreeNode root;
        

    

}
