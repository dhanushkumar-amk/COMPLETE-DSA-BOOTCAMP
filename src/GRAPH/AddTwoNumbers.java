package GRAPH;

// https://leetcode.com/problems/add-two-numbers/description/
//leetcode 2

public class AddTwoNumbers {

    public class ListNode {
      int val;
      ListNode next;

      ListNode() {

      }

      ListNode(int val) {
          this.val = val;
      }

      ListNode(int val, ListNode next) {
          this.val = val;
          this.next = next;
      }
  }

  
}
